            jLabel13.setText(db.user.getString(4));
            jLabel14.setText(db.user.getString(5));
            jLabel15.setText(db.user.getString(6));
            db.yazarBildirim(Integer.parseInt(db.user.getString(1)));
            String strDate = db.user.getString(9);
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            Date dateStr = (Date) formatter.parse(strDate);
            Date onay = new Date();
            SimpleDateFormat sdf2;
            DefaultListModel dim = new DefaultListModel();
            String onayTarihi;
            while (db.zamanLI.next()) {
                onayTarihi = db.zamanLI.getString(12);
                sdf2 = new SimpleDateFormat("yyyy-MM-dd");
                onay = (Date) formatter.parse(onayTarihi);
                if ((dateStr.compareTo(onay)) <= 0) {
                    dim.addElement(db.zamanLI.getString(1) + " nolu gönderiniz onaylanmıştır.");
                }
            }
            jList1.setModel(dim);
