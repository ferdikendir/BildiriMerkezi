    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int row = jTable2.getSelectedRow();
        Object[] options = {"Tamamını Gör", "İptal"};
        int n = JOptionPane.showOptionDialog(null,
                db.dizim[row][0] + " nolu gönderiniz hakkındaki işlemler.?",
                null, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        if (n == JOptionPane.YES_OPTION) {
            db.getBildiri(Integer.parseInt(db.dizim[row][0]));
            bildiri bil = new bildiri();
            bil.setVisible(true);
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        int row = jTable1.getSelectedRow();
        Object[] options = {"Tamamını Gör", "Güncelle", "İptal"};
        int n = JOptionPane.showOptionDialog(null,
                db.dizi[row][0] + " nolu gönderiniz hakkındaki işlemler.?",
                null, JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        if (n == JOptionPane.YES_OPTION) {
            db.getBildiri(Integer.parseInt(db.dizi[row][0]));
            bildiri bil = new bildiri();
            bil.setVisible(true);

        } else if (n == JOptionPane.NO_OPTION) {
            db.getBildiri(Integer.parseInt(db.dizi[row][0]));
            try {
                if (db.bildiri.getString(8).equals("") == false && db.bildiri.getString(10).equals("") == false) {
                    JOptionPane.showMessageDialog(null, "Bildirinizin hakem işleri tamamandığında dolayı güncelleme yapamazsınız.");
                } else {
                    bildiriGuncelle bilgun = new bildiriGuncelle();
                    bilgun.setVisible(true);
                }
            } catch (SQLException ex) {
                Logger.getLogger(yazar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

